<?php 

// Operadores Lógicos

// AND 
echo "AND: ";
echo 0 && 0; // 0
echo 0 && 1; // 0
echo 1 && 1; // 1
echo 1 && 1; // 1

echo PHP_EOL;

// OR
echo "OR: ";
echo 0 || 0; // 0
echo 0 || 1; // 1
echo 1 || 1; // 1
echo 1 || 1; // 1

echo PHP_EOL;

// OR
echo "XOR: ";
echo 0 ^ 0; // 0
echo 0 ^ 1; // 1
echo 1 ^ 1; // 1
echo 1 ^ 1; // 0
